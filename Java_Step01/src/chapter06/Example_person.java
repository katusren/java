package chapter06;

public class Example_person {

	// 멤버 변수
	String name;
	float height;
	float weight;

	public Example_person() {

	}

	public Example_person(String name) {
		this.name = name;
	}

	public Example_person(String name, float height, float weight) {
		this.name = name;
		this.height = height;
		this.weight = weight;
	}

}
